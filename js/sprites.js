// ─── Sprite sheet assembly ────────────────────────────────────────────────────

const SPRITE_DATA = [
  SPR_DETECTIVE, SPR_GENTLEMAN, SPR_LADY,     SPR_URCHIN,
  SPR_CONSTABLE, SPR_BARKEEP,   SPR_MERCHANT, SPR_DOCTOR,
  SPR_CLOSE,     SPR_TAVERN,    SPR_STREET,   SPR_DOCKS,
  SPR_COURTHOUSE,SPR_GRAVEYARD, SPR_MARKET,   SPR_INTERIOR,
  SPR_LETTER,    SPR_KNIFE,     SPR_BOTTLE,   SPR_COIN,
  SPR_KEY,       SPR_BOOK,      SPR_HANDKERCHIEF, SPR_WATCH,
  SPR_FOOTPRINT, SPR_DOCUMENT,  SPR_RING,     SPR_HAT,
  SPR_NEWSPAPER, SPR_PHOTOGRAPH,SPR_SKULL,    SPR_CANDLE,
];

// Sprite sheet: 8 sprites wide × 4 rows, each sprite 32×32 = 256×128 total
const SHEET_COLS = 8;
const SPRITE_SIZE = 32;

/** Module-private sprite sheet canvas */
let _sheetCanvas = null;
let _sheetCtx = null;

/**
 * Initialises the sprite sheet by rendering all sprite data to an offscreen canvas.
 * Must be called once before getSprite().
 */
export function initialiseSpriteCanvas() {
  const totalWidth  = SPRITE_SIZE * SHEET_COLS;
  const totalHeight = SPRITE_SIZE * (SPRITE_DATA.length / SHEET_COLS);

  if (typeof OffscreenCanvas !== 'undefined') {
    _sheetCanvas = new OffscreenCanvas(totalWidth, totalHeight);
  } else {
    // Fallback: regular canvas appended to a hidden element
    _sheetCanvas = document.createElement('canvas');
    _sheetCanvas.width  = totalWidth;
    _sheetCanvas.height = totalHeight;
    const hidden = document.createElement('div');
    hidden.style.cssText = 'position:absolute;left:-9999px;top:-9999px;width:0;height:0;overflow:hidden;';
    hidden.appendChild(_sheetCanvas);
    document.body.appendChild(hidden);
  }

  _sheetCtx = _sheetCanvas.getContext('2d');

  // Parse palette colours into RGBA arrays for fast pixel writing
  const rgbPalette = PALETTE.map(hex => {
    const n = parseInt(hex.slice(1), 16);
    return [(n >> 16) & 0xff, (n >> 8) & 0xff, n & 0xff];
  });

  SPRITE_DATA.forEach((pixelData, index) => {
    const col = index % SHEET_COLS;
    const row = Math.floor(index / SHEET_COLS);
    const ox  = col * SPRITE_SIZE;
    const oy  = row * SPRITE_SIZE;

    const imgData = _sheetCtx.createImageData(SPRITE_SIZE, SPRITE_SIZE);
    const buf = imgData.data;

    for (let i = 0; i < SPRITE_SIZE * SPRITE_SIZE; i++) {
      const pi = pixelData[i];
      const [r, g, b] = rgbPalette[pi];
      const bi = i * 4;
      buf[bi]     = r;
      buf[bi + 1] = g;
      buf[bi + 2] = b;
      buf[bi + 3] = 255; // fully opaque
    }

    _sheetCtx.putImageData(imgData, ox, oy);
  });
}

/**
 * Returns a <canvas> element containing the named sprite, scaled to displaySize px.
 * @param {string} name — sprite name (must be in SPRITE_INDEX)
 * @param {number} displaySize — output canvas size in CSS pixels
 * @returns {HTMLCanvasElement}
 */
export function getSprite(name, displaySize = 128) {
  const index = SPRITE_INDEX[name] ?? SPRITE_INDEX['detective'];
  const col = index % SHEET_COLS;
  const row = Math.floor(index / SHEET_COLS);
  const sx  = col * SPRITE_SIZE;
  const sy  = row * SPRITE_SIZE;

  const canvas = document.createElement('canvas');
  canvas.width  = displaySize;
  canvas.height = displaySize;
  canvas.style.imageRendering = 'pixelated';

  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;
  ctx.drawImage(_sheetCanvas, sx, sy, SPRITE_SIZE, SPRITE_SIZE, 0, 0, displaySize, displaySize);

  return canvas;
}

/**
 * Returns a data URL for the named sprite at the given display size.
 * Useful for setting as img.src or CSS background.
 * @param {string} name
 * @param {number} displaySize
 * @returns {string}
 */
export function getSpriteDataURL(name, displaySize = 128) {
  return getSprite(name, displaySize).toDataURL();
}
