// ─── Sprite pixel data ────────────────────────────────────────────────────────
// Each sprite: 1024 bytes. One byte per pixel (row-major). Value = palette index.
// Helper: parse a compact multi-line string of single-digit palette indices.

/**
 * Parses a compact string of palette digits into a Uint8Array[1024].
 * Whitespace is stripped. Must produce exactly 1024 values.
 */
function px(str) {
  const digits = str.replace(/\s+/g, '');
  const arr = new Uint8Array(1024);
  for (let i = 0; i < 1024; i++) {
    arr[i] = digits.charCodeAt(i) - 48; // '0'.charCodeAt(0) === 48
  }
  return arr;
}

